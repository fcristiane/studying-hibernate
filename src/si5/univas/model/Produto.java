package si5.univas.model;

public class Produto {

	private int cod;
	private String nome;
	private int qtd;
	
	public int getCod() {
		return cod;
	}
	
	@Override
	public String toString() {
		return ""+nome+"";
	}

	public void setCod(int cod) {
		this.cod = cod;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public int getQtd() {
		return qtd;
	}
	
	public void setQtd(int qtd) {
		this.qtd = qtd;
	}
	
}
